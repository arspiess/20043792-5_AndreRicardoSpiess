<ins>História</ins><br>
<br>
<strong>Churros</strong> é uma iguaria que existe há muitos séculos. Sua origem é incerta. Alguns dizem que foram os portugueses que trouxeram a receita, quando voltavam da China na época da dinastia Ming. Outros dizem que foram os árabes que a trouxeram, quando invadiram a península ibérica.<br>
O alimento ganhou popularidade em São Paulo a partir de 1974, com a chegada de um estabelecimento de origem espanhola. De início, ele era comercializado em pedaços, e com a sugestão de clientes, tiveram a ideia de rechear com doce de leite. Com a grande aceitação, eles ampliaram para novos recheios e formatos.<br>
Hoje eles se tornaram comuns, com a receita com recheio que é genuinamente brasileira, estão em diversos lugares e barracas. São incríveis para serem oferecidos em festas e eventos.<br>
